package exemplos;

public class ClasseMath_teste {
	 public static void main(String [] args){
		// armazena o valor de PI
		 double pi = Math.PI; 
		 
		 // armazena a raiz de 25
		 double raiz = Math.sqrt(25.0);
		 
		 // armazena a potencia = 5^3
		 double potencia = Math.pow(5.0, 3.0);

		 // armazena o logaritimo de 1000.0
		 double logaritmo = Math.log10(1000.0);
		 
		 //exibir os resultados
		 System.out.println("pi = " + pi);
		 System.out.println("raiz = " + raiz);
		 System.out.println("potência = " + potencia);
		 System.out.println("logaritmo = " + logaritmo);
	 }
}
